# 1. A라는 전구회사에서 생산하는 전구의 수명은 800일이고 표준편차는 40일인 정규분포를
# 따른다고 한다. 이때 전구의 수명이 750일 이하일 확률을 구하시오
# N(800, 40^2)
pnorm(750, 800, 40)     # 0.1056498

# 2. 근무기간을 조사하였더니, 평균은 11년이고 분산이 16년인 정규분포를 따른다고
# N(11, 4^2)
# 2-1) 20년 이상 근무한 종업원의 비율
1 - pnorm(20, 11, 4)    # 0.01222447
# 2-2) 근무연수가 가장 오래된 10%의 종업원은 이 회사에서 몇 년 이상 근무
qnorm(0.9, 11, 4)       # 16.12621

# 3.학생들의 수학성적은 평균이 70이고 표준편차가 8인 정규분포
# N(70, 8^2)
# 점수가 80점 이상이고 90점 이하인 학생의 비율
pnorm(90, 70, 8) - pnorm(80, 70, 8)     # 0.09944011

# 4. N(1.5, 2)
# H(t) = P(t ≤ X ≤ t+1)
h = function(t) {
    return(pnorm(t+1, 1.5, 2) - pnorm(t, 1.5, 2))
}
h(0) + h(2)             # 0.3493326
