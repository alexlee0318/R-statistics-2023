# 2. 한 축구 선수가 페널티킥을 차면 5번 중 4번을 성공한다고 한다. 10번의 페널티킥을 차서 7번 성공할 확률을 구하시오
# B(10, 0.8)
dbinom(7, 10, 0.8)      # 0.2013266

facto = function(n) {
    if (n == 0)
        return(1)
    return(n*facto(n-1))
}
facto(10) / (facto(7) * facto(3))
120*0.8^7*0.2^3

